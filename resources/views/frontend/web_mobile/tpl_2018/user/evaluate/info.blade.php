    <script type="text/javascript">
        var options = {};
        $().ready(function() {
            var options = {};
            if ($("#gallery a").length > 0) {
                $("#gallery a").photoSwipe(options);
            }
            //图片上传
            $("body").on('click', '.upload-image', function() {
                var $this = $(this);
                var sku_id = $this.data('sku-id');
                var image_show = $this.parents('.user-comment-box').find('.user-comment-pic');
                $.localResizeIMG({
                    callback: function(image) {
                        image_show.append("<li><a href='"+image.data.url+"'><img src='"+image.data.url+"'></a><span herf='javascript:void(0)' class='del-pic' data-sku-id='" + sku_id + "'><input type='hidden' value='"+image.data.path+"'></span></li>");
                        var imgpath = [];
                        $.each(image_show.find('input'), function(i, v) {
                            imgpath.push(v.value);
                        });
                        $("#images_" + sku_id).val(imgpath.join(','));
                        $.msg(image.message);
                        if (image_show.find('img').size() >= 5) {
                            //$this.addClass('hide');
                            $this.css('display', 'none');
                        }
                        if ($("#gallery a").length > 0) {
                            $("#gallery a").photoSwipe(options);
                        }
                    }
                });
            });

            $("body").on('click', '.del-pic', function() {
                $this = $(this);
                $.confirm("确定要删除这张图片吗？", function(s) {
                    if (s) {
                        var image_show = $this.parents('.user-comment-pic');
                        var sku_id = $this.data('sku-id');
                        var imgpath = [];
                        imgpath = $("#images_" + sku_id).val().split(',');
                        imgpath.splice($.inArray($this.find('input').val(), imgpath), 1);
                        $("#images_" + sku_id).val(imgpath.join(','));
                        $this.parent('li').remove();
                        if (image_show.find('li a img').size() < 5) {
                            //$this.addClass('hide');
                            image_show.parents('.user-comment-box').find('.upload-image').css('display', '');
                        }
                        $.msg("删除成功!");
                        if ($("#gallery a").length > 0) {
                            $("#gallery a").photoSwipe(options);
                        }
                    }
                });
            });
        });
    </script>
